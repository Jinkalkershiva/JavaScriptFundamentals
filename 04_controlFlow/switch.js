//switch case

// cases as number
const month =3
switch(month){
    case 1:
        console.log(`Jan`);
        break;
    case 2:
        console.log(`Feb`);
        break;
    case 3:
        console.log(`Feb`); //Feb
        break;
    default:
        break;
}

//cases as string
const monthName = `march`
switch(monthName){
    case `jan`:
        console.log(1);
        break;
    case `feb`:
        console.log(2);
        break;
    case `march`:
        console.log(3); //3
        break;
    default:
        break;
}

